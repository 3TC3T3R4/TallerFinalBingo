CREATE DATABASE BINGO;

USE BINGO;

CREATE TABLE USER(
id_player int NOT NULL auto_increment,
PRIMARY KEY(id_player)
)ENGINE=INNODB;


CREATE TABLE GAME(
id_game INT AUTO_INCREMENT,
PRIMARY KEY(id_game)
)ENGINE=INNODB;


CREATE TABLE BALLOT(
id_ballot INT AUTO_INCREMENT,
PRIMARY KEY(id_ballot)
)ENGINE=INNODB;


